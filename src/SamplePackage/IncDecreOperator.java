package SamplePackage;

public class IncDecreOperator {

	public static void main(String[] args) {
		int a=10;
		int b =20;
		int c=++a;
		int d=b--;
		System.out.println(a);//-->11
		System.out.println(b);//-->19
		System.out.println(c);//11
		System.out.println(d);//20
		System.out.println(a);//11
		System.out.println(b);//19
		
	}

}




